package pojo.request.createbooking.fmc;

public class ChildDetails {

	public String fullname;
	public int age;
	public String gender;
	public String height;
	public String weight;
	public String complexion;
	public String clothing;
	public String birth_signs;
	public String other_details;
	public String image_file_key;
	public String nickname;
	public String getFullname() {
		return fullname;
	}
	public void setFullname(String fullname) {
		this.fullname = fullname;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getHeight() {
		return height;
	}
	public void setHeight(String height) {
		this.height = height;
	}
	public String getWeight() {
		return weight;
	}
	public void setWeight(String weight) {
		this.weight = weight;
	}
	public String getComplexion() {
		return complexion;
	}
	public void setComplexion(String complexion) {
		this.complexion = complexion;
	}
	public String getClothing() {
		return clothing;
	}
	public void setClothing(String clothing) {
		this.clothing = clothing;
	}
	public String getBirth_signs() {
		return birth_signs;
	}
	public void setBirth_signs(String birth_signs) {
		this.birth_signs = birth_signs;
	}
	public String getOther_details() {
		return other_details;
	}
	public void setOther_details(String other_details) {
		this.other_details = other_details;
	}
	public String getImage_file_key() {
		return image_file_key;
	}
	public void setImage_file_key(String image_file_key) {
		this.image_file_key = image_file_key;
	}
	public String getNickname() {
		return nickname;
	}
	public void setNickname(String nickname) {
		this.nickname = nickname;
	}
	
}
